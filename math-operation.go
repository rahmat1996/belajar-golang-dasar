package main

import "fmt"

func main() {
	var angka = 10 + 10
	fmt.Println(angka)

	var a = 10
	var b = 20
	var c = a * b
	fmt.Println(c)

	//augmented assignment
	var i = 10
	i += 10
	fmt.Println(i)

	//unary operator
	var j = 1
	j++
	fmt.Println(j)

	var positive = 100
	var negative = -100
	fmt.Println(positive)
	fmt.Println(negative)

}
